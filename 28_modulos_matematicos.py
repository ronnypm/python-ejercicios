# math.ceil(x)
# Devuelve el entero más pequeño mayor o igual a x (redondea hacia arriba).

print('math.ceil(x)')
print(math.ceil(3.2))# -> 4
print(math.ceil(-2.3))# -> -2
print('-' * 30)

# ----------------------------------------------------------

print('math.factorial(x)')

print(math.factorial(5)) # -> Resultado: 120 (porque 5×4×3×2×1 = 120)


